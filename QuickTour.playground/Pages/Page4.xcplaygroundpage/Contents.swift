 ## キーボードショートカット
 */
"コードを実行するときは、キーボードショートカットを使うと便利です。"
"プレイボタンを使わずに、コードを実行できます。"
/*:
 次のコードを記述してみましょう。
 ````
 pressShiftAndReturnKey()
 ````
 */

"コードを記述したら、「shiftキーを押しながら、returnキー」を押してください。"
"すると、その行までのコードを実行した上で、カーソルが次の行に移動します。"
"プレイボタンを使うよりも、すばやくコードを実行できます。"
"ここで、青い行のコードは「Playgroundをリセットせずに編集しても安全である」ことを意味します。"
"反対に「青くない行のコード」を編集すると、それは実行済みのコードを変更していることになります。"
"その場合、実行結果も変わる可能性があるので、Playgroundはリセットしておくべきです。"
/*:
 青くない行にある「先ほど記述したコード」を次のように変更してください。
 ````
 pressShiftAndReturnKeyAgain()
 ````
*/
"すると、Playgroundは「青くない行のコード」が編集されたので、自動的に実行結果をリセットします。"
"したがって、実行結果はグレーアウトされて、コードの行も青くなりました。"
"Playgroundの基本的な使い方は、以上です。"
"たくさんコードを書いて、少しずつ慣れていきましょう。"
