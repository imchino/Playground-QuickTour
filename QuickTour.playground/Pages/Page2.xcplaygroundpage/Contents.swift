 ## Xcodeの補完機能を使って、コードを記述する
 */
"Xcodeには入力補完機能が用意されています。"
"補完機能を使用することで、コードをすばやく正確に記述できます。"
/*:
 次のコードを記述してください。\
 `ju...`と入力した時点で、候補が表示されます。
 ````
 justDoIt()
 ````
 */

"Tabキーを押して候補を絞り込んでから、returnキーで決定してください。"
"コードを記述できたら、プレイボタンを押して実行します。"
"なお、「コードを実行した結果」はインラインで表示することもできます。"
"リザルトサイドバーにある短形のボタンをクリックしましょう。"
"すると、「コードを実行した結果」がエディタ領域にインラインで表示されます。"
"もう一度、同じボタンをクリックすると非表示になります。"
