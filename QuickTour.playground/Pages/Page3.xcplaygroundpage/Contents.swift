 ## 実行できるコードの場所
 */
"灰色のプレイボタンは、「コードが既に実行済みである」ことを示していました。"
"他に、その行が「コードのトップレベルではない」場合もプレイボタンは灰色になります。"
"プレイボタンの色に注目してください。"
"ここはトップレベルなので、青いプレイボタンです。"
do {
    "波括弧{}の内側は「トップレベルではない」ので、プレイボタンが灰色になります。"
    "このコードを実行するには、プレイボタンが青くなる「閉じ括弧の行」にポインタを移動します。"
    justDoIt()
}
"トップレベルではないコードは、関数やループといった構文で記述されます。"
"覚えておきましょう。"
